package serie2.ex4;

public class Livre {
    private String name;
    private int year;
    private String author;

    public Livre(){
        this.name=name;
        this.year=year;
        this.author=author;
    }

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }

    public String getAuthor() {
        return author;
    }
}
